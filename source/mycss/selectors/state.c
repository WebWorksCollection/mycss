/* <combinator> */
bool mycss_selectors_state_combinator(mycss_result_t* result, mycss_selectors_t* selectors, mycss_selectors_entry_t* selector, mycss_token_t* token)
{
    switch (token->type) {
        case MyCSS_TOKEN_TYPE_COLUMN: {
            mycss_selectors_parser_selector_combinator(result, selectors, selector, token);
            ////printf("mycss_selectors_state_combinator_column\n");  /* End of selector */
            result->parser = mycss_parser_token;
            break;
        }
        case MyCSS_TOKEN_TYPE_DELIM: {
            if(*token->data == '+') {
                mycss_selectors_parser_selector_combinator(result, selectors, selector, token);
                ////printf("mycss_selectors_state_combinator_plus\n");  /* End of selector */
                result->parser = mycss_parser_token;
            }
            else if(*token->data == '>') {
                mycss_selectors_parser_selector_combinator(result, selectors, selector, token);
                ////printf("mycss_selectors_state_combinator_greater_than\n");  /* End of selector */
                result->parser = mycss_parser_token;
                selectors->state = mycss_selectors_state_combinator_greater_than;
                
                if(result->parser != mycss_selectors_state_token_skip_whitespace)
                    result->parser = mycss_selectors_state_token_skip_whitespace;
            }
            else if(*token->data == '~') {
                mycss_selectors_parser_selector_combinator(result, selectors, selector, token);
                ////printf("mycss_selectors_state_combinator_tilde\n");  /* End of selector */
                result->parser = mycss_parser_token;
            }
            else {
                result->parser = mycss_parser_token;
                return false;
            }
            break;
        }
        default: {
            result->parser = mycss_parser_token;
            return false;
            break;
        }
    }
    
    return true;
}

bool mycss_selectors_state_combinator_greater_than(mycss_result_t* result, mycss_selectors_t* selectors, mycss_selectors_entry_t* selector, mycss_token_t* token)
{
    if(token->type == MyCSS_TOKEN_TYPE_DELIM) {
        if(*token->data == '>') {
            mycss_selectors_parser_selector_combinator(result, selectors, selector, token);
            ////printf("mycss_selectors_state_combinator_greater_than_greater_than\n");  /* End of selector */
            result->parser = mycss_parser_token;
        }
        else {
            result->parser = mycss_parser_token;
            return false;
        }
    }
    else {
        result->parser = mycss_parser_token;
        return false;
    }
    
    return true;
}



